package com.manish.Java8.Practice.Example20;

class Notes {
    private long tagId;
    private String tagName;

    public Notes(long tagId, String tagName) {
        this.tagId = tagId;
        this.tagName = tagName;
    }

    public long getTagId() {
        return tagId;
    }

    public String getTagName() {
        return tagName;
    }
}
